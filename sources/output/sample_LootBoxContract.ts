export class LootBoxContract implements Contract {
    
    static async init(owner: Address) {
        return await LootBoxContract_init(owner);
    }
    
    static async fromInit(owner: Address) {
        const init = await LootBoxContract_init(owner);
        const address = contractAddress(0, init);
        return new LootBoxContract(address, init);
    }
    
    static fromAddress(address: Address) {
        return new LootBoxContract(address);
    }
    
    readonly address: Address; 
    readonly init?: { code: Cell, data: Cell };
    readonly abi: ContractABI = {
        types:  LootBoxContract_types,
        getters: LootBoxContract_getters,
        receivers: LootBoxContract_receivers,
        errors: LootBoxContract_errors,
    };
    
    private constructor(address: Address, init?: { code: Cell, data: Cell }) {
        this.address = address;
        this.init = init;
    }
    
    async send(provider: ContractProvider, via: Sender, args: { value: bigint, bounce?: boolean| null | undefined }, message: 'withdraw' | 'play' | Deploy) {
        
        let body: Cell | null = null;
        if (message === 'withdraw') {
            body = beginCell().storeUint(0, 32).storeStringTail(message).endCell();
        }
        if (message === 'play') {
            body = beginCell().storeUint(0, 32).storeStringTail(message).endCell();
        }
        if (message && typeof message === 'object' && !(message instanceof Slice) && message.$$type === 'Deploy') {
            body = beginCell().store(storeDeploy(message)).endCell();
        }
        if (body === null) { throw new Error('Invalid message type'); }
        
        await provider.internal(via, { ...args, body: body });
        
    }
    
    async getCounter(provider: ContractProvider) {
        let builder = new TupleBuilder();
        let source = (await provider.get('counter', builder.build())).stack;
        let result = source.readBigNumber();
        return result;
    }
    
    async getBiggestWin(provider: ContractProvider) {
        let builder = new TupleBuilder();
        let source = (await provider.get('biggestWin', builder.build())).stack;
        let result = source.readBigNumber();
        return result;
    }
    
    async getOwner(provider: ContractProvider) {
        let builder = new TupleBuilder();
        let source = (await provider.get('owner', builder.build())).stack;
        let result = source.readAddress();
        return result;
    }
    
}